"""File for simple math functions"""


def add(num_1, num_2):
    """Returns the sum of two  numers

    Args:
        num_1 (int): The first number to add
        num_2 (int): The second number to add

    Returns:
        int: Returns the sum of num_1 and num_2
    """
    return num_1 + num_2


def sub(num_1, num_2):
    return num_1 - num_2


def multi(num_1, num_2):
    return num_1 * num_2
